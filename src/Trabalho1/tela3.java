    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ContaPoupanca cp= ContaPoupanca.getInstancia1();
        Contas conta= Contas.getInstancia();
        ContaEspecial cs= ContaEspecial.getInstancia1();
//        if(Integer.parseInt(jTextField2.getText())==conta.getNumero()||Integer.parseInt(jTextField2.getText())==cp.getNumero())
//        {
//            JOptionPane.showMessageDialog(null,"Número de conta já cadastrado");
//            return;
//        }
//       
            if(jTextField1.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Nome inválido");
                return;
            }
            else
            {
                cs.setNome(jTextField1.getText());
                try {
                    if(Integer.parseInt(jTextField2.getText())==conta.getNumero()||Integer.parseInt(jTextField2.getText())==cp.getNumero())
                    {
                      JOptionPane.showMessageDialog(null,"Número de conta já cadastrado");
                        return;
                    }
                    else{
                    cs.setNumero(Integer.parseInt(jTextField2.getText()));
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Número inválido");
                    return;
                }
                try {
                    cs.setSaldo(Integer.parseInt(jTextField3.getText()));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Saldo inválido");
                    return;
                }
                try {
                    cs.setLimite((Integer.parseInt(jTextField4.getText())));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Taxa de reajuste inválida");
                    return;
                }
                try {
                    cs.setMulta(Double.parseDouble(jTextField5.getText()));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Multa inválida");
                    return;
                }
            }
            new Final().setVisible(true);
            dispose();
       
    }//GEN-LAST:event_jButton1ActionPerformed

